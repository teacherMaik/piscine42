int	ft_is_prime(int nb)
{
	int	i;
	int	res;

	if (nb <= 1)
		return (0);
	else if (nb == 2147483647)
		return (1);
	i = 2;
	while (i * i <= nb)
	{
		res = nb % i;
		if (res == 0)
			return (0);
		i++;
	}
	return (1);
}
