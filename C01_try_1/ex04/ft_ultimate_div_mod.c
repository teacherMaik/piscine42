void	ft_ultimate_div_mod(int *a, int *b)
{
	int	y;
	int	x;

	if (b != 0)
	{
		y = *a / *b;
		x = *a % *b;
		*a = y;
		*b = x;
	}
	else
	{
		*a = 0;
		*b = 0;
	}
}
